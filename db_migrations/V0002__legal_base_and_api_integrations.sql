
-- Правовая база ЕЦСУ: юрисдикции, документы, статьи
CREATE TABLE t_p38294978_open_source_program_.egsu_legal_jurisdictions (
  id SERIAL PRIMARY KEY,
  code VARCHAR(10) UNIQUE NOT NULL,
  name VARCHAR(255) NOT NULL,
  country VARCHAR(100),
  type VARCHAR(50) NOT NULL, -- international, national, regional
  created_at TIMESTAMP DEFAULT NOW()
);

CREATE TABLE t_p38294978_open_source_program_.egsu_legal_documents (
  id SERIAL PRIMARY KEY,
  jurisdiction_id INTEGER REFERENCES t_p38294978_open_source_program_.egsu_legal_jurisdictions(id),
  code VARCHAR(50) UNIQUE NOT NULL,
  title VARCHAR(500) NOT NULL,
  category VARCHAR(100) NOT NULL, -- constitution, criminal, civil, procedural, international
  description TEXT,
  adopted_year INTEGER,
  language VARCHAR(20) DEFAULT 'ru',
  is_active BOOLEAN DEFAULT true,
  created_at TIMESTAMP DEFAULT NOW()
);

CREATE TABLE t_p38294978_open_source_program_.egsu_legal_articles (
  id SERIAL PRIMARY KEY,
  document_id INTEGER REFERENCES t_p38294978_open_source_program_.egsu_legal_documents(id),
  article_number VARCHAR(50) NOT NULL,
  title VARCHAR(500),
  content TEXT NOT NULL,
  tags TEXT[],
  created_at TIMESTAMP DEFAULT NOW()
);

-- Индексы для поиска
CREATE INDEX idx_legal_articles_content ON t_p38294978_open_source_program_.egsu_legal_articles USING gin(to_tsvector('russian', content));
CREATE INDEX idx_legal_documents_category ON t_p38294978_open_source_program_.egsu_legal_documents(category);

